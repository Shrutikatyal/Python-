def succ(N):
    '''
    OBJECTIVE: To find the successor of a number.
    INPUT PARAMETER:
          N: a non-negative number whose successor is to be calculated.
    OUTPUT:
          Successor of non-negative number 'N'.
    '''
    #Approach: succ(N) = N+1

    if N>=0:
        return N+1
    print("Error: ",N," < 0.")
    return


